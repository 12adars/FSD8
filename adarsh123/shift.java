package adarsh123;

public class shift 
{
    public static void main(String[] args) 
    {
        int x=8;
        System.out.println(x<<3);//signed left shift
        System.out.println(x>>0);//8 signed right shift
        System.out.println(x>>1);//4
        System.out.println(x>>2);//2
        System.out.println(x>>3);//1
        //general formulae of right shift 
        //(2^n)/2
        //>>>-unsigned right shift operator.
    }
}
